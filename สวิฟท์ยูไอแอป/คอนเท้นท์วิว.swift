import SwiftUI


// MARK: - คอนเท้นท์วิว (ContentView)

struct คอนเท้นท์วิว: มุมมอง {
    
    @State private var นับจำนวน: จำนวนเต็ม = 0
    
    var body: some มุมมอง {
        
        เรียงแนวตั้ง(การจัดเรียง: .กลาง, ระยะห่าง: 20.0) {

            ซ้อนทับ {
                รูปภาพ(ชื่อระบบ: "circle.fill")
                    .ปรับขนาดได้()
                    .กรอบ(width: 150.0, height: 150.0)
                    .สีด้านหน้า(สี.มิ้นท์)
                    .ความทึบแสง(0.3)
                
                ข้อความ("\(นับจำนวน)")
                    .รูปแบบอักษร(.ระบบ(size: 48.0, weight: .หนา, design: .มน))
                    .สีด้านหน้า(สี.มิ้นท์)
            }
            .ระยะขอบ(.ล่าง, 20.0)
            
            เรียงแนวนอน(ระยะห่าง: 30.0) {
                
                ปุ่ม("เพิ่ม") {
                    self.นับจำนวน += 1
                }
                .ปุ่มสไตล์เน้นขอบเขต()
                
                ปุ่ม("รีเซ็ต") {
                    self.นับจำนวน = 0
                }
                .ปุ่มสไตล์ขอบเขต()
                .สีเน้น(สี.แดง)
            }
            
            ตัวเว้นระยะ()
            
            ข้อความ("แอปพลิเคชัน SwiftUI ในสไตล์ไทย")
                .รูปแบบอักษร(.หัวข้อรอง)
                .สีด้านหน้า(สี.รอง)
        }
        .ระยะขอบ(30.0)
    }
}

